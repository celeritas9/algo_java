package com.practice;

/**
 * 
 *
 * @author dshah
 * @since 
 */
public class Node
{
	private String name;
	private int value;
	private Node next;

	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

	public Node getNext() {
		return next;
	}

	public void setNext(Node next) {
		this.next = next;
	}

	public Node(String name_, int value_, Node next_) 
	{
		name = name_;
		value = value_;
		next = next_;
	}
}
